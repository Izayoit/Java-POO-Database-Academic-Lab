
package modelo;



public class Ciudad {
        String ciudad;
        int Nhospitales;
        int Npobladores;
        int Nescuelas;

    public Ciudad(String ciudad, int Nhospitales, int Npobladores, int Nescuelas) {
        this.ciudad = ciudad;
        this.Nhospitales = Nhospitales;
        this.Npobladores = Npobladores;
        this.Nescuelas = Nescuelas;
    }

    public String getciudad() {
        return ciudad;
    }

    public void setciudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public int getNhospitales() {
        return Nhospitales;
    }

    public void setNhospitales(int Nhospitales) {
        this.Nhospitales = Nhospitales;
    }

    public int getNpobladores() {
        return Npobladores;
    }

    public void setNpobladores(int Npobladores) {
        this.Npobladores = Npobladores;
    }

    public int getNescuelas() {
        return Nescuelas;
    }

    public void setNescuelas(int Nescuelas) {
        this.Nescuelas = Nescuelas;
    }

   
        
        
    }
        
        
 
    
    
    
    
    
    
    

