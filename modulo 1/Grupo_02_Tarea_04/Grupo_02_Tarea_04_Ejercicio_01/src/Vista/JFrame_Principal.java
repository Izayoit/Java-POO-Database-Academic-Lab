    private void solo_decimales_positivos(java.awt.event.KeyEvent evt) {
        char caracter = evt.getKeyChar();
        if(Character.isLetter(evt.getKeyChar()) || caracter == '-' || Character.isWhitespace(evt.getKeyChar())){
            evt.consume();
        }
        if(caracter == '.'){
            if (txt_Peso.getText().contains(".")){
                evt.consume();
            }
        }
    }
    
