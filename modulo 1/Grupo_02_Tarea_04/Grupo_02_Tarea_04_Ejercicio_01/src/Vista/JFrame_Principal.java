    private void btn_AñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AñadirActionPerformed
        if(txt_Peso.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Inserte todos los datos");
        }
        
        String Item = cbo_Raza.getSelectedItem().toString();
        
        //TOTAL DE VACAS
        
        if(Item == "Shorton"){
            Cont_S++;
            txt_Shorton.setText(Cont_S+"");
        }
        if(Item == "Yorkshire"){
            Cont_Y++;
            txt_Yorkshire.setText(Cont_Y+"");
        }
        if(Item == "Holstein"){
            Cont_Ho++;
            txt_Holstein.setText(Cont_Ho+"");
        }
        if(Item == "Hereford"){
            Cont_He++;
            txt_Hereford.setText(Cont_He+"");
        }
        
        int NS = Integer.parseInt(txt_Shorton.getText());
        int NY = Integer.parseInt(txt_Yorkshire.getText());
        int NHo = Integer.parseInt(txt_Holstein.getText());
        int NHe = Integer.parseInt(txt_Hereford.getText());
        txt_Total_Vacas.setText(NS+NY+NHo+NHe+"");
        
        //PESOS DE LAS VACAS  
        double Peso = Double.parseDouble(txt_Peso.getText());
        
        //PROMEDIO TOTAL
      double numero = 1;
        numero++;
        if(txt_Promedio.getText().isEmpty()){
            txt_Promedio.setText(Peso+"");
            
        }else  {
            double n = Double.parseDouble(txt_Promedio.getText());
            txt_Promedio.setText((double)((n+Peso)/numero)+"");
            
        }
        
       
        
        //MAYOR
        double PMaS = Double.parseDouble(txt_Mayor_Shorton.getText());
        double PMaY = Double.parseDouble(txt_Mayor_Yorkshire.getText());
        double PMaHo = Double.parseDouble(txt_Mayor_Holstein.getText());
        double PMaHe = Double.parseDouble(txt_Mayor_Hereford.getText());
        
        if(Item == "Shorton"){
            if(Peso>PMaS){
                txt_Mayor_Shorton.setText(Peso+"");
            }else{
                txt_Mayor_Shorton.setText(PMaS+"");
            }
            
        }
        if(Item == "Yorkshire"){
            if(Peso>PMaY){
                txt_Mayor_Yorkshire.setText(Peso+"");
            }else{
                txt_Mayor_Yorkshire.setText(PMaY+"");
            }
            
        }
        if(Item == "Holstein"){
            if(Peso>PMaHo){
                txt_Mayor_Holstein.setText(Peso+"");
            }else{
                txt_Mayor_Holstein.setText(PMaHo+"");
            }
            
        }
        if(Item == "Hereford"){
            if(Peso>PMaHe){
                txt_Mayor_Hereford.setText(Peso+"");
            }else{
                txt_Mayor_Hereford.setText(PMaHe+"");
            }
            
        }
        if(PMaS>PMaY && PMaS>PMaHo && PMaS>PMaHe && PMaS>Peso){
            txt_Mayor_Peso.setText(PMaS+"");
        }else if(PMaY>PMaS && PMaY>PMaHo && PMaY>PMaHe && PMaY>Peso){
            txt_Mayor_Peso.setText(PMaY+"");
        }else if(PMaHo>PMaS && PMaHo>PMaY && PMaHo>PMaHe && PMaHo>Peso){
            txt_Mayor_Peso.setText(PMaHo+"");
        }else if (PMaHe>PMaS && PMaHe>PMaY && PMaHe>PMaHo && PMaHe>Peso){
            txt_Mayor_Peso.setText(PMaHe+"");
        }else{
            txt_Mayor_Peso.setText(Peso+"");
        }
        //MENOR
        double PMeS = Double.parseDouble(txt_Menor_Shorton.getText());
        double PMeY = Double.parseDouble(txt_Menor_Yorkshire.getText());
        double PMeHo = Double.parseDouble(txt_Menor_Holstein.getText());
        double PMeHe = Double.parseDouble(txt_Menor_Hereford.getText());
        
        if(Item == "Shorton"){
            if( !txt_Mayor_Shorton.getText().isEmpty()){
                txt_Menor_Shorton.setText(Peso+"");
            }
            
        }
        if(Item == "Yorkshire"){
            if(!txt_Mayor_Yorkshire.getText().isEmpty()){
                txt_Menor_Yorkshire.setText(Peso+"");
            }
            
        }
        if(Item == "Holstein"){
            if(!txt_Mayor_Holstein.getText().isEmpty()){
                txt_Menor_Holstein.setText(Peso+"");
            }
            
        }
        if(Item == "Hereford"){
            if(!txt_Mayor_Hereford.getText().isEmpty()){
                txt_Menor_Hereford.setText(Peso+"");
            }
            
        }
        if(PMeS<PMeY && PMeS<PMeHo && PMeS<PMeHe ){
            txt_Menor_Peso.setText(PMeS+"");
        }else if(PMeY<PMeS && PMeY<PMeHo && PMeY<PMeHe ){
            txt_Menor_Peso.setText(PMeY+"");
        }else if(PMeHo<PMeS && PMeHo<PMeY && PMeHo<PMeHe ){
            txt_Menor_Peso.setText(PMeHo+"");
        }else if (PMeHe<PMeS && PMeHe<PMeY && PMeHe<PMeHo ){
            txt_Menor_Peso.setText(PMeHe+"");
        }else if (txt_Menor_Peso.getText().isEmpty() ){
          double Peso1 = Double.parseDouble(txt_Peso.getText());
        txt_Menor_Peso.setText(Peso+"");
        }
           
        
        
        //GANADERÍA DE CARNE Y PROD. DE LECHE
        
        if(Item == "Shorton" && Peso<=500){
            Cont_Carne_S++;
            txt_Carne_Shorton.setText(Cont_Carne_S+"");
        }
        if(Item == "Yorkshire" && Peso<=500){
            Cont_Carne_Y++;
            txt_Carne_Yorkshire.setText(Cont_Carne_Y+"");
        }
        if(Item == "Holstein" && Peso<=500){
            Cont_Carne_Ho++;
            txt_Carne_Holstein.setText(Cont_Carne_Ho+"");
        }
        if(Item == "Hereford" && Peso<=500){
            Cont_Carne_He++;
            txt_Carne_Hereford.setText(Cont_Carne_He+"");
        }
        //PROD. DE LECHE
        //<>
        if(Item == "Shorton" && Peso>500){
            Cont_Leche_S++;
            txt_Leche_Shorton.setText(Cont_Leche_S+"");
        }
        if(Item == "Yorkshire" && Peso>500){
            Cont_Leche_Y++;
            txt_Leche_Yorkshire.setText(Cont_Leche_Y+"");
        }
        if(Item == "Holstein" && Peso>500){
            Cont_Leche_Ho++;
            txt_Leche_Holstein.setText(Cont_Leche_Ho+"");
        }
        if(Item == "Hereford" && Peso>500){
            Cont_Leche_He++;
            txt_Leche_Hereford.setText(Cont_Leche_He+"");
        }
    }//GEN-LAST:event_btn_AñadirActionPerformed

