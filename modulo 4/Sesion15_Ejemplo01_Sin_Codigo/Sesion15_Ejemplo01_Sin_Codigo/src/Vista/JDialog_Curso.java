    private void btn_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditarActionPerformed
        Estado_Controles(true);
        int fila = tbl_datos.getSelectedRow();
        if(fila != -1){
            int CodCurso = Integer.parseInt(
                    tbl_datos.getValueAt(fila, 0).toString()
                );
            String Nombre = tbl_datos.getValueAt(fila, 1).toString();
            Elemento = new Curso();
            Elemento.setCodCurso(CodCurso);
            Elemento.setNombre(Nombre);
            txt_Nombre.setText(Nombre);
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione un registro");
        }
        Tipo_Ope = 2;
    }//GEN-LAST:event_btn_EditarActionPerformed

    private void btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarActionPerformed
        Estado_Controles(false);
        String nombre = txt_Nombre.getText();
        switch(Tipo_Ope){
            case 1: Elemento = new Curso();
                    Elemento.setNombre(nombre);
                    CursoDAO.Insertar(Elemento);
                break;
            case 2: Elemento.setNombre(nombre);
                    CursoDAO.Actualizar(Elemento);
        }
        Actualizar_Tabla();
        txt_Nombre.setText("");
        Tipo_Ope = 0;
    }//GEN-LAST:event_btn_GuardarActionPerformed

