    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        String nombre = txt_nombre.getText();
        String apellido = txt_apellido.getText();
        int dni = Integer.parseInt(txt_dni.getText());
        int edad = Integer.parseInt(txt_edad.getText());
        String genero = txt_genero.getText();
        String region =txt_region.getText();
        String seleccionado = (String) cbo_tipo_inter.getSelectedItem();

        switch (seleccionado) {
            case "FibraObtica":

            JOptionPane.showMessageDialog(this, "exitoso");
            break;
            case "4G":
            // Código para Ana
            JOptionPane.showMessageDialog(this, "exitoso");
            break;
            case "5G":
            // Código para Luis
            JOptionPane.showMessageDialog(this, "Exitoso");
            break;
            default:
            JOptionPane.showMessageDialog(this, "Exitoso");
            break;
        }
        usuarios datos1 = new usuarios(nombre, apellido, dni, edad, genero, region, seleccionado);
        objarreglousuarios.registrar(datos1);
        objarreglousuarios.listar(tbl_table);

    }//GEN-LAST:event_btn_registrarActionPerformed

