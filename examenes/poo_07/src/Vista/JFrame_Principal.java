    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int codigo = Integer.parseInt(txt_codigo.getText());
        int precio = Integer.parseInt(txt_precio.getText());
        Producto objProducto = new Producto(codigo);
        Precio objPrecio = new Precio(precio);
        Tienda objTienda = new Tienda(codigo, precio);
        objArreglo_Producto.Agregar(objProducto);
        objArreglo_Precio.Registrar(objPrecio);
        objArreglo_Tienda.Agregar(objTienda);
        objArreglo_Tienda.Listar(tbl_datos);
        int mayor = objArreglo_Tienda.MayorPrecio();
        int codigomayor = objArreglo_Tienda.Buscar(mayor);
        int menor = objArreglo_Tienda.MenorPrecio();
        int codigoMenor = objArreglo_Tienda.Buscar(menor);
        System.out.println("Mayor Precio: " + mayor + " // " + "Codigo: " + codigomayor + "\n" + "Menor Precio: " + menor + " // " + "Codigo: " + codigoMenor);
    }//GEN-LAST:event_jButton1ActionPerformed

